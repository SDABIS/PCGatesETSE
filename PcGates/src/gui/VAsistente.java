    private void btnEditaUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditaUsuarioActionPerformed
        if(tablaUsu.getSelectedRow()<0)
            fa.muestraExcepcion("Se debe de seleccionar a un usuario para realizar esta opreación");
        else{
            ModeloTablaAsistente m;
            m = (ModeloTablaAsistente) tablaUsu.getModel();
            if(m.getRowCount()>0){
                Cliente c = m.obtenerCliente(tablaUsu.getSelectedRow());
                fa.iniciaEditarUsuario(c, m);
            }
        }
    }//GEN-LAST:event_btnEditaUsuarioActionPerformed

    private void tablaUsuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuMouseClicked
        if(tablaUsu.getSelectedRow()>=0){
            btnEditaUsuario.setEnabled(true);
            btnDespachar.setEnabled(true);
        }
            
    }//GEN-LAST:event_tablaUsuMouseClicked
 
