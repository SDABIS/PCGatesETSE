    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ModeloTablaAsistente m;
        m = (ModeloTablaAsistente) tablaUsu.getModel();
        m.setFilas(fa.consultarClientes(textoNombre.getText(), textoId.getText()));
        if (m.getRowCount() > 0) { //Si la tabla tiene elementos
            tablaUsu.setRowSelectionInterval(0, 0);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnDespacharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDespacharActionPerformed
        // TODO add your handling code here:
        if(tablaUsu.getRowCount()==0){
            return;
        }
        if(tablaUsu.getSelectedRow()<0){
            fa.muestraExcepcion("Se debe de seleccionar a un usuario para realizar esta operación");
        }
        else{
            ModeloTablaAsistente m;
            m = (ModeloTablaAsistente) tablaUsu.getModel();
            if(m.getRowCount()>0){
                Cliente c = m.obtenerCliente(tablaUsu.getSelectedRow());
                fa.visualizarDespachar(c, asistente);
            }
        }
    }//GEN-LAST:event_btnDespacharActionPerformed

