    private void botonAnadirComponenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirComponenteActionPerformed
        
        boolean hayNombre = (textoNombreComponente.getText() != null) && (!textoNombreComponente.getText().isEmpty());
        boolean hayPrecio = (textoPrecioComponente.getText() != null) && (!textoPrecioComponente.getText().isEmpty());
        boolean hayMarca = (textoMarcaComponente.getText() != null) && (!textoMarcaComponente.getText().isEmpty());
        boolean hayDescripcion = (textoDescripcionComponente.getText() != null) && (!textoDescripcionComponente.getText().isEmpty());
        boolean valido = hayNombre && hayPrecio && hayMarca && hayDescripcion;
        if(!valido) {
            fa.muestraExcepcion("Todos los campos deben estar llenos.");
            return;
        }
        
        try{
            Double.parseDouble(textoPrecioComponente.getText());
        }
        catch(NumberFormatException e) {
            fa.muestraExcepcion("Introduzca un número en el campo de Precio.");
            return;
        }
        
        String nombre = textoNombreComponente.getText();
        double precio = Double.parseDouble(textoPrecioComponente.getText());
        if(precio <= 0) {
            fa.muestraExcepcion("El precio no puede ser negativo.");
            return;
        }
        String marca = textoMarcaComponente.getText();
        String descripcion = textoDescripcionComponente.getText();
        
        String tipo = (String)comboComponente.getSelectedItem();
        Componente comp = null;
        switch (tipo) {
            case "Alimentación":
                tipo = "alimentacion";
                boolean hayPotencia = (textoPotenciaAlim.getText() != null) && (!textoPotenciaAlim.getText().isEmpty());
                valido = valido && hayPotencia;
                try{
                    if(Double.parseDouble(textoPotenciaAlim.getText()) <= 0) {
                        fa.muestraExcepcion("La potencia no puede ser negativa.");
                        return;
                    }
                }
                catch(NumberFormatException e) {
                    fa.muestraExcepcion("Introduzca un número en el campo de Potencia.");
                    return;
                }
                boolean modular;
                if(((String)comboModularAlim.getSelectedItem()).equals("Sí")) modular = true;
                else modular = false;

                comp = new Alimentacion(Double.parseDouble(textoPotenciaAlim.getText()), modular, nombre, precio, marca, descripcion);
            break;
                
            case "CPU":
                tipo = "cpus";
                boolean haySockets = (textoSocketsCpu.getText() != null) && (!textoSocketsCpu.getText().isEmpty());
                boolean hayCores = (textoCoresCpu.getText() != null) && (!textoCoresCpu.getText().isEmpty());
                boolean hayThreads = (textoThreadsCpu.getText() != null) && (!textoThreadsCpu.getText().isEmpty());
                boolean hayFrecuencia = (textoFrecuenciaCpu.getText() != null) && (!textoFrecuenciaCpu.getText().isEmpty());
                boolean hayTdp = (textoTdpCpu.getText() != null) && (!textoTdpCpu.getText().isEmpty());
                boolean hayMicro = (textoMicroCpu.getText() != null) && (!textoMicroCpu.getText().isEmpty());
                valido = valido && haySockets && hayCores && hayThreads && hayThreads && hayFrecuencia && hayTdp && hayMicro;
                try{
                    Integer.parseInt(textoCoresCpu.getText());
                    Integer.parseInt(textoThreadsCpu.getText());
                    Double.parseDouble(textoFrecuenciaCpu.getText());
                    Double.parseDouble(textoTdpCpu.getText());
                }
                catch(NumberFormatException e) {
                    fa.muestraExcepcion("Introduzca un número en los campos de Cores, Threads, Frecuencia y TDP.");
                    return;
                }

                comp = new CPU(textoSocketsCpu.getText(), Integer.parseInt(textoCoresCpu.getText()),
                        Integer.parseInt(textoThreadsCpu.getText()), Double.parseDouble(textoFrecuenciaCpu.getText()),
                        Double.parseDouble(textoTdpCpu.getText()), textoMicroCpu.getText(),
                        nombre, precio, marca, descripcion);
                break;
                
            case "Disco duro":
                tipo = "discosduros";
                boolean hayCapacidad = (textoCapacidadDisco.getText() != null) && (!textoCapacidadDisco.getText().isEmpty());
                boolean hayVelocidad = (textoVelocidadDisco.getText() != null) && (!textoVelocidadDisco.getText().isEmpty());

                valido = valido && hayCapacidad && hayVelocidad;
                try{
                    if(Integer.parseInt(textoCapacidadDisco.getText()) <= 0) {
                        fa.muestraExcepcion("La capacidad no puede ser negativa.");
                        return;
                    }
                    if(Double.parseDouble(textoVelocidadDisco.getText()) <= 0) {
                        fa.muestraExcepcion("La velocidad no puede ser negativa.");
                        return;
                    }
                    
                }
                catch(NumberFormatException e) {
                    fa.muestraExcepcion("Introduzca un número en los campos de Capacidad y Velocidad.");
                    return;
                }

                comp = new DiscoDuro((String)comboTipoDisco.getSelectedItem(),Double.parseDouble(textoVelocidadDisco.getText()),
                        Integer.parseInt(textoCapacidadDisco.getText()), nombre, precio, marca, descripcion);
                break;
                
            case "Placa base":
                tipo = "placasbase";
                boolean hayChipset = (textoChipsetPlaca.getText() != null) && (!textoChipsetPlaca.getText().isEmpty());
                boolean hayTamano = (textoTamanoPlaca.getText() != null) && (!textoTamanoPlaca.getText().isEmpty());

                valido = valido && hayChipset && hayTamano;
                try{
                    Double.parseDouble(textoTamanoPlaca.getText());
                }
                catch(NumberFormatException e) {
                    fa.muestraExcepcion("Introduzca un número en el campo de Tamaño.");
                    return;
                }

                comp = new PlacaBase(textoChipsetPlaca.getText(),Double.parseDouble(textoTamanoPlaca.getText()),
                        nombre, precio, marca, descripcion);
                break;
            case "GPU":
                boolean hayNucleos = (textoNucleosGpu.getText() != null) && (!textoNucleosGpu.getText().isEmpty());
                boolean hayProceso = (textoProcesoGpu.getText() != null) && (!textoProcesoGpu.getText().isEmpty());
                boolean hayFrecuencia3 = (textoFrecuenciaGpu.getText() != null) && (!textoFrecuenciaGpu.getText().isEmpty());

                valido = valido && hayNucleos && hayProceso && hayFrecuencia3;
                try{
                    if(Integer.parseInt(textoNucleosGpu.getText()) <= 0) {
                        fa.muestraExcepcion("Los núcleos deben ser positivos.");
                        return;
                    }
                    if(Double.parseDouble(textoFrecuenciaGpu.getText()) <= 0) {
                        fa.muestraExcepcion("La frecuencia debe ser positiva.");
                        return;
                    }
                    if(Integer.parseInt(textoProcesoGpu.getText()) <= 0) {
                        fa.muestraExcepcion("El proceso debe ser positivo.");
                        return;
                    }
                }
                catch(NumberFormatException e) {
                    fa.muestraExcepcion("Introduzca un número en los campos de Núcleos, Frecuencia y Proceso.");
                    return;
                }

                comp = new GPU(Integer.parseInt(textoNucleosGpu.getText()),Double.parseDouble(textoFrecuenciaGpu.getText()),
                        Integer.parseInt(textoProcesoGpu.getText()), nombre, precio, marca, descripcion);
                tipo = "gpus";
                
                break;
            case "Periférico":
                tipo = "perifericos";
                boolean hayTipo = (textoTipoPerif.getText() != null) && (!textoTipoPerif.getText().isEmpty());
                valido = valido && hayTipo;


                comp = new Periferico(textoTipoPerif.getText(), nombre, precio, marca, descripcion);
                break;
            case "RAM":
                tipo = "rams";
                
                boolean hayFrecuencia2 = (textoFrecuenciaRam.getText() != null) && (!textoFrecuenciaRam.getText().isEmpty());
                boolean hayMemoria = (textoMemoriaRam.getText() != null) && (!textoMemoriaRam.getText().isEmpty());

                valido = valido && hayFrecuencia2 && hayMemoria;
                try{
                    if(Double.parseDouble(textoMemoriaRam.getText()) <= 0) {
                        fa.muestraExcepcion("La memoria debe ser positiva.");
                        return;
                    }
                    if(Double.parseDouble(textoFrecuenciaRam.getText()) <= 0) {
                        fa.muestraExcepcion("La frecuencia debe ser positiva.");
                        return;
                    }
                }
                catch(NumberFormatException e) {
                    fa.muestraExcepcion("Introduzca un número en los campos de Memoria y Frecuencia.");
                    return;
                }

                comp = new RAM((String)comboTipoRam.getSelectedItem(), Double.parseDouble(textoMemoriaRam.getText()),
                        Double.parseDouble(textoFrecuenciaRam.getText()), (String)comboFormatoRam.getSelectedItem(),
                        nombre, precio, marca, descripcion);
                
                break;
            case "Refrigeración":
                tipo = "refrigeracion";
                boolean hayTamano2 = (textoTamanoRefri.getText() != null) && (!textoTamanoRefri.getText().isEmpty());

                valido = valido && hayTamano2;
                try{
                    Integer.parseInt(textoTamanoRefri.getText());
                }
                catch(NumberFormatException e) {
                    fa.muestraExcepcion("Introduzca un número en el campo de Tamaño.");
                    return;
                }

                comp = new Refrigeracion((String)comboTipoRefri.getSelectedItem(), Integer.parseInt(textoTamanoRefri.getText()),
                        nombre, precio, marca, descripcion);
                break;
        }
        if(!valido) {
            fa.muestraExcepcion("Todos los campos deben estar llenos.");
            return;
        }
        
        fa.anadirComponente(tipo, comp);
    }//GEN-LAST:event_botonAnadirComponenteActionPerformed

    private void botonAnadirPCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirPCActionPerformed
        boolean hayNombre = (textoNombrePC.getText() != null) && (!textoNombrePC.getText().isEmpty());
        boolean hayPrecio = (textoPrecioPC.getText() != null) && (!textoPrecioPC.getText().isEmpty());
        if((!hayNombre) || (!hayPrecio)) {
            fa.muestraExcepcion("Todos los campos deben estar llenos.");
            return;
        }
        try{
            Integer.parseInt(textoPrecioPC.getText());
        }
        catch(NumberFormatException e) {
            fa.muestraExcepcion("Introduzca un número en el campo de Precio.");
            return;
        }
        
        fa.anadirPc(textoNombrePC.getText(), Integer.parseInt(textoPrecioPC.getText()));
    }//GEN-LAST:event_botonAnadirPCActionPerformed

