    private void comboComponenteEjemplarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboComponenteEjemplarActionPerformed
        String comp = (String)comboComponenteEjemplar.getSelectedItem();
        if(comp.equals("Alimentación")) comp = "alimentacion";
        else if(comp.equals("Disco duro")) comp = "discosduros";
        else if(comp.equals("CPU")) comp = "cpus";
        else if(comp.equals("Placa base")) comp = "placasbase";
        else if(comp.equals("GPU")) comp = "gpus";
        else if(comp.equals("Periférico")) comp = "perifericos";
        else if(comp.equals("RAM")) comp = "rams";
        else if(comp.equals("Refrigeración")) comp = "refrigeracion";
        
        java.util.ArrayList<String> comps = fa.consultarComponentes(comp);
        ModeloListaStrings mListaC = (ModeloListaStrings)listaEjemplar.getModel();
        mListaC.setElementos(comps);
    }//GEN-LAST:event_comboComponenteEjemplarActionPerformed

    private void botonAnadirEjemplarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirEjemplarActionPerformed
        if(listaEjemplar.getSelectedIndex() != -1) {
            String nombre = ((ModeloListaStrings)listaEjemplar.getModel()).getElementos().get(listaEjemplar.getSelectedIndex());
            fa.anadirEjemplar(nombre);
            
        }
        else fa.muestraExcepcion("Seleccione un componente al que añadir ejemplar.");
    }//GEN-LAST:event_botonAnadirEjemplarActionPerformed

